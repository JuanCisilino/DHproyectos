public class AutoyMoto  {

    private Integer pesoAuto;
    private Integer ruedasAuto;
    private Integer pesoMoto;
    private Integer ruedasMoto;


    public AutoyMoto() {
        this.pesoAuto = 1000;
        this.ruedasAuto = 4;
        this.pesoMoto = 300;
        this.ruedasMoto = 2;
    }

    public Integer getPesoMoto() {
        return pesoMoto;
    }

    public Integer getRuedasMoto() {
        return ruedasMoto;
    }
    public Integer getPesoAuto() {
        return pesoAuto;
    }

    public Integer getRuedasAuto() {
        return ruedasAuto;
    }
}
